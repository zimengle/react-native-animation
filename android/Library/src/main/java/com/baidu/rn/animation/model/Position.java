package com.baidu.rn.animation.model;


public class Position {
    private Float x;
    private Float y;

    public Float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
